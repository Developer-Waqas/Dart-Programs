void main() {
  double feet, meter, result;

  //Foot in meter
  print("Foot in Meter");
  feet = 0.3048;
  meter = 20;
  result = meter * feet;

  print("20 Foots in Meter: $result m");

  //Meter in foots
  print("Meter in foots");
  meter = 20;
  feet = 3.28084;
  result = feet * meter;

  print("20 meters in Foots: $result Ft");
}
